#!/usr/bin/env node
'use strict'

// Generates the gamemode script-signing keypair and installs the signer into
// the game server's deploy dir (build/dist/server, same resolution as
// src/build.js):
//   - signing-private.pem : Ed25519 private key (PKCS#8 PEM)
//   - sign-gamemode.js    : loaded at runtime by the gamemode's signHelper
//                           (require(path.join(process.cwd(), 'sign-gamemode.js')))
// then prints the entries to add to skymp5-backend/data/public-keys.json.
//
// Signature format (from skymp5-client serverJsVerificationService.ts):
// the last line of the script must be `// skymp:sig:y:<keyId>:<sig>` where
// <sig> is a base64 Ed25519 detached signature over everything before that
// line's leading "\n", verified with crypto.verify(null, src, pem, sig).
// sweetTaffyEvalService matches the prefix "// skymp:sig:y:GM" and looks the
// key up WITHOUT the leading "GM", while gamemodeUpdateService /
// gamemodeEventSourceService use the full key id - so the key id must start
// with "GM" and public-keys.json needs BOTH spellings mapped to the same PEM.

const fs = require('fs')
const path = require('path')
const crypto = require('crypto')

const repoRoot = path.resolve(__dirname, '..', '..')
const buildDir = process.env.ALDUINAK_BUILD_DIR || path.join(repoRoot, 'build')
const deployDir = path.join(buildDir, 'dist', 'server')
const pemFile = path.join(deployDir, 'signing-private.pem')
const signerFile = path.join(deployDir, 'sign-gamemode.js')

if (fs.existsSync(pemFile) && !process.argv.includes('--force')) {
  console.error(`${pemFile} already exists - re-run with --force to replace it`)
  console.error('(replacing the key invalidates the public key already published in public-keys.json)')
  process.exit(1)
}

const keyId = 'GM' + crypto.randomBytes(6).toString('hex')
const { publicKey, privateKey } = crypto.generateKeyPairSync('ed25519')
const privatePem = privateKey.export({ type: 'pkcs8', format: 'pem' })
const publicPem = publicKey.export({ type: 'spki', format: 'pem' })

const signer = `'use strict'

// Generated by server-manager/tools/gen-signing-keys.js - do not edit.
// Loaded by the gamemode's signHelper from the game server's cwd; signs each
// served script so the client's ServerJsVerificationService can verify it.

const fs = require('fs')
const path = require('path')
const crypto = require('crypto')

const KEY_ID = '${keyId}'
const privateKey = fs.readFileSync(path.join(__dirname, 'signing-private.pem'), 'utf8')

exports.signScript = function signScript(src) {
  const sig = crypto.sign(null, Buffer.from(src, 'utf8'), privateKey).toString('base64')
  return \`\${src}\\n// skymp:sig:y:\${KEY_ID}:\${sig}\`
}
`

fs.mkdirSync(deployDir, { recursive: true })
fs.writeFileSync(pemFile, privatePem, { mode: 0o600 })
fs.writeFileSync(signerFile, signer)

console.log(`key id : ${keyId}`)
console.log(`wrote  : ${pemFile}`)
console.log(`wrote  : ${signerFile}`)
console.log('')
console.log('Merge these entries into skymp5-backend/data/public-keys.json')
console.log('(create the file with just this object if it does not exist):')
console.log('')
console.log(JSON.stringify({ [keyId]: publicPem, [keyId.slice(2)]: publicPem }, null, 2))
console.log('')
console.log('Then restart AlduinakBackend and AlduinakGameServer.')
